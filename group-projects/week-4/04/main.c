int get_file_size(FILE *fh) {
  struct stat st;
  fstat(fileno(fh), &st);
  int size = st.st_size;

  return size;
};

void xor_with_key(unsigned char *pdf_data, int pdf_size,
                  unsigned char key[32][32]) {
  int row = 0;
  int col = 0;

  for (int i = 0; i < pdf_size; i++) {
    key[row][col] ^= pdf_data[i];
    col++;

    if (col == 32) {
      col = 0;
      row++;
    }
    if (row == 32) {
      row = 0;
      col = 0;
    }
  }
}

void write_file_key(char *key_file_name, unsigned char *pdf_data,
                    int pdf_size) {

  FILE *fh = fopen(key_file_name, "wb");

  xor_with_key(pdf_data, pdf_size, og_key);

  fwrite(og_key, 1, sizeof(og_key), fh);
  fclose(fh);
}

void print_key(unsigned char key[32][32]) {
  for (int row = 0; row < 32; row++) {
    for (int col = 0; col < 32; col++) {

      unsigned char value = key[row][col];
      Pixel *pixel = (Pixel *)&value;
      if (pixel->background) {

        printf("%s%c ", get_ansi_bg(pixel->color), get_symbol(pixel->symbol));
      } else {
        printf("%s%c ", get_ansi_color(pixel->color),
               get_symbol(pixel->symbol));
      }
    }
    printf("%s\n", ANSI_RESET);
  }
}

bool is_modified(unsigned char key_data[32][32]) {
  for (int row = 0; row < 32; row++) {
    for (int col = 0; col < 32; col++) {
      if (key_data[row][col] != og_key[row][col])
        return true;
    }
  }
  return false;
}

int main(int argc, char **argv) {

  if (argc != 4) {
    printf("Usage: %s <file> <key>\n", argv[0]);
    return 1;
  }

  FILE *fh = fopen(argv[2], "rb");
  int size = get_file_size(fh);
  unsigned char *pdf_data = (unsigned char *)malloc(size);
  fread(pdf_data, 1, size, fh);

  if (argv[1][1] == 'g') {

    print_key(og_key);
    write_file_key(argv[3], pdf_data, size);
    free(pdf_data);
    fclose(fh);
  }

  else if (argv[1][1] == 'c') {

    FILE *key_fh = fopen(argv[3], "rb");
    unsigned char generated_key[32][32];
    fread(generated_key, 1, sizeof(generated_key), key_fh);

    print_key(og_key);

    printf("\n\n\n");

    xor_with_key(pdf_data, size, generated_key);
    print_key(generated_key);

    bool is_key_changed = is_modified(generated_key);

    if (!is_key_changed) {
      printf("%s\n\n\n\nno modification detected\n", ANSI_RESET);
    } else {
      printf("%s\nModification detected\n", ANSI_RESET);
    }

    fclose(key_fh);
  }

  return 0;
}